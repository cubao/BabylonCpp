#ifndef BABYLON_CONSTANTS_H
#define BABYLON_CONSTANTS_H

namespace BABYLON {

namespace Math {

/** Constants **/
// Euler's e and related
constexpr float E       = 2.71828182845904523536f;  // Euler's constant e
constexpr float LOG2E   = 1.44269504088896340736f;  // log2(e)
constexpr float LOG10E  = 0.434294481903251827651f; // log10(e)
constexpr float LN2     = 0.693147180559945309417f; // ln(2)
constexpr float LN10    = 2.30258509299404568402f;  // ln(10)
constexpr float PI      = 3.14159265358979323846f;  // pi
constexpr float PI2     = 6.28318530717958647692f;  // pi * 2
constexpr float PI_2    = 1.57079632679489661923f;  // pi/2
constexpr float PI_4    = 0.785398163397448309616f; // pi/4
constexpr float SQRT2   = 1.41421356237309504880f;  // sqrt(2)
constexpr float SQRT2_2 = 0.707106781186547524401f; // sqrt(2)/2

// Used by Babylon Math
/**
 * Constant used to convert a value to gamma space
 */
constexpr float ToGammaSpace = 1.f / 2.2f;
/**
 * Constant used to convert a value to linear space
 */
constexpr float ToLinearSpace = 2.2f;
/**
 * Constant used to define the minimal number value in Babylon.js
 */
constexpr float Epsilon = 0.001f;

} // end of namespace Math

} // end of namespace BABYLON

#endif // end of BABYLON_CONSTANTS_H
